    private void pintarTabla(){
            
        String encabezado[]={"NIT","Direccion","Telefono","Nombre"};
        DefaultTableModel t= new DefaultTableModel(encabezado,Proyecto_A.Proveedor.size());
        jTable1.setModel(t);
        TableModel tabla=jTable1.getModel();
        
        for (int i = 0; i < Proyecto_A.Proveedor.size(); i++) {
            Proveedor p = Proyecto_A.Proveedor.get(i);
            tabla.setValueAt(p.Nit, i, 0);
            tabla.setValueAt(p.direccion, i, 1);
            tabla.setValueAt(p.telefono, i, 2);
            tabla.setValueAt(p.nombre, i, 3);
        }
        }

