    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         int eliminar =jTable1.getSelectedRow();
        if (eliminar>0) {
            if (JOptionPane.showConfirmDialog(this, "Esta seguro de eliminar")==0) {
              Proyecto_A.Proveedor.remove(eliminar);
              pintarTabla();
            }
        } else {
            JOptionPane.showConfirmDialog(this, "Seleccione un campo para eliminar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRow()>=0){
            Proveedor Proveedor = Proyecto_A.Proveedor.get(jTable1.getSelectedRow());
        jTextField1.setText(Proveedor.Nit);
        jTextField2.setText(Proveedor.direccion);
        jTextField3.setText(Proveedor.telefono);
        jTextField4.setText(Proveedor.telefono);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
           if (jTable1.getSelectedRow() >= 0) {
       
        Proveedor proveedor = Proyecto_A.Proveedor.get(jTable1.getSelectedRow());
        
        // Actualizar los datos del proveedor con los valores de los campos de texto
        proveedor.Nit = jTextField1.getText();
        proveedor.direccion = jTextField2.getText();
        proveedor.telefono = jTextField3.getText();
        proveedor.nombre = jTextField4.getText();
        
        
        pintarTabla();
        
        JOptionPane.showMessageDialog(this, "Proveedor modificado exitosamente");
    } else {
        JOptionPane.showMessageDialog(this, "Por favor seleccione un proveedor", 
        "Advertencia", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_jButton5ActionPerformed

