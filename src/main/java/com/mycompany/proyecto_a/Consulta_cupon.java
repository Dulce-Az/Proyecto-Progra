    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int eliminar =jTable1.getSelectedRow();
        if (eliminar>0) {
            if (JOptionPane.showConfirmDialog(this, "Esta seguro de eliminar")==0) {
              Proyecto_A.cuponesdescuento.remove(eliminar);
              pintarTabla();
            }
        } else {
            JOptionPane.showConfirmDialog(this, "Seleccione un cupon valido para eliminar");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow()>=0) {
            cupon =Proyecto_A.cuponesdescuento.get(jTable1.getSelectedRow());
            jTextField1.setText(cupon.CodigoCupon);
            jTextField2.setText(String.valueOf(cupon.Valordescuento));
            
            if (cupon.esPorcentaje) {
                jComboBox1.setSelectedIndex(0);
            } else {
                jComboBox1.setSelectedIndex(1);
            }
            SimpleDateFormat sdf=new SimpleDateFormat("dd/MM/yyyy");
            jTextField3.setText(sdf.format(cupon.Fechavencimiento));
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         if (cupon != null) {
        cupon.CodigoCupon = jTextField1.getText();
        cupon.Valordescuento = Double.parseDouble(jTextField2.getText());

        String tipo = jComboBox1.getSelectedItem().toString();

        if (tipo.equals("Porcentaje")) {
            cupon.esPorcentaje = true;
        } else {
            cupon.esPorcentaje = false;
        }
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            cupon.Fechavencimiento = sdf.parse(jTextField3.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Formato de fecha inv√°lido (use dd/MM/yyyy)");
            return;
        }
        pintarTabla();
        JOptionPane.showMessageDialog(this, "Cupon modificado");

         } 
    }//GEN-LAST:event_jButton4ActionPerformed

   

