    private void pintarTabla(){
    String encabezado[]={"Codigo", "Valor","Tipo","Vencimiento"};
        DefaultTableModel t=new DefaultTableModel(encabezado, Proyecto_A.cuponesdescuento.size());
        jTable1.setModel(t);
        TableModel tabla=jTable1.getModel();
        
        SimpleDateFormat sdf =new SimpleDateFormat();
        
        for (int i = 0; i < Proyecto_A.cuponesdescuento.size(); i++) {
          Cupon c=Proyecto_A.cuponesdescuento.get(i);
          tabla.setValueAt(c.CodigoCupon, i, 0);
          tabla.setValueAt(String.format("Q%,.2f", c.Valordescuento), i, 1);
          tabla.setValueAt(c.esPorcentaje ? "Porcentaje" : "Monto Fijo", i, 2);
          tabla.setValueAt(sdf.format(c.Fechavencimiento), i, 3);
        }
    } 
    
  
