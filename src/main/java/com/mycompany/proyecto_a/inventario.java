    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int eliminar =jTable1.getSelectedRow();
        
        if (eliminar > 0) {
            if (JOptionPane.showConfirmDialog(this , "Esta seguro de eliminar")==0) {
                Proyecto_A.inventarioLibros.remove(eliminar);
                pintarTabla();
            }
        } else {
            JOptionPane.showMessageDialog(this, "seleccione un campo valido para eliminar");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         if (jTable1.getSelectedRow()>=0) {
            lib = Proyecto_A.inventarioLibros.get(jTable1.getSelectedRow());
            jTextField1.setText(lib.titulo);
            jTextField2.setText(lib.autor);
            jTextField3.setText(lib.genero);
            jTextField4.setText(String.valueOf(lib.precio));
            jTextField5.setText(String.valueOf(lib.cantidadStock));
        } 
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
      if (lib !=null) {
            lib.titulo=jTextField1.getText();
            lib.autor=jTextField2.getText();
            lib.genero=jTextField3.getText();
            lib.precio=Integer.parseInt(jTextField4.getText());
            lib.cantidadStock=Integer.parseInt(jTextField5.getText());
            
            pintarTabla();
            JOptionPane.showMessageDialog(this,"Libro modificado correctamente");
        } 
    }//GEN-LAST:event_jButton3ActionPerformed

