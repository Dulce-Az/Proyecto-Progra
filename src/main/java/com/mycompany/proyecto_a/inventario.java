    private void pintarTabla(){
    String[] encabezados = {"Título", "Autor", "Género", "Precio ", "Stock"};
        DefaultTableModel model = new DefaultTableModel(encabezados, Proyecto_A.inventarioLibros.size());
        jTable1.setModel(model);
        
        TableModel tabla = jTable1.getModel();
        
        for (int i = 0; i < Proyecto_A.inventarioLibros.size(); i++) {
            libro l = Proyecto_A.inventarioLibros.get(i);
            
            tabla.setValueAt(l.titulo, i, 0);
            tabla.setValueAt(l.autor, i, 1);
            tabla.setValueAt(l.genero, i, 2);
            tabla.setValueAt(l.precio, i, 3);
            tabla.setValueAt(l.cantidadStock, i, 4);
        }
    }
