    private void CargaMasivaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CargaMasivaActionPerformed
        // TODO add your handling code here:
       
        try {
            String ruta = obtenerRuta();
            if (ruta != null) {
                ArrayList<Cupon> cupones = leer(ruta);
                Proyecto_A.cuponesdescuento.addAll(cupones);
                JOptionPane.showMessageDialog(this, 
                    "Se cargaron " + cupones.size() + " cupones", 
                    "Éxito", 
                    JOptionPane.INFORMATION_MESSAGE);
                this.dispose(); // Cierra la ventana después de cargar
            }
        } catch (IOException ex) {
            Logger.getLogger(AñadirCupon.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(AñadirCupon.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_CargaMasivaActionPerformed

   

