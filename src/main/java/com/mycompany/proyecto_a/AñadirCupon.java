    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
           String codigo = jTextField1.getText();
            String valorStr = jTextField2.getText();
            String tipo = jComboBox1.getSelectedItem().toString();
            String fechaStr = jTextField3.getText(); 
            
         if (codigo.isEmpty() || valorStr.isEmpty() || fechaStr.isEmpty()) {
          JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
            return;
            } 
        double valor = Double.parseDouble(valorStr);
        if (valor <= 0) {
            JOptionPane.showMessageDialog(this, "El valor debe ser mayor a cero", "Error", JOptionPane.ERROR_MESSAGE);
            return;
            }
       Date fechaVencimiento=new java.text.SimpleDateFormat("dd/MM/yyyy").parse(fechaStr);
        if (fechaVencimiento.before(new Date())) {
         JOptionPane.showMessageDialog(this, "La fecha de vencimiento debe ser futura", "Error", JOptionPane.ERROR_MESSAGE);
          return;
            }
         boolean esPorcentaje = tipo.equals("Porcentaje");
            
            // Crear nuevo cupón
            Cupon nuevoCupon = new Cupon();
            nuevoCupon.CodigoCupon = codigo;
            nuevoCupon.Valordescuento = valor;
            nuevoCupon.esPorcentaje = esPorcentaje;
            nuevoCupon.Fechavencimiento = fechaVencimiento;
            
            // Agregar a la lista global
            Proyecto_A.cuponesdescuento.add(nuevoCupon);
            
            JOptionPane.showMessageDialog(this, "Cupón creado exitosamente");
        } catch (NumberFormatException ex) {
           JOptionPane.showMessageDialog(this, "El valor debe ser un numero valido","Error",JOptionPane.ERROR_MESSAGE);
        }
           catch(java.text.ParseException ex){
           JOptionPane.showMessageDialog(this, "Formato de fecha inválido (use dd/MM/yyyy)", "Error", JOptionPane.ERROR_MESSAGE); 
        }
        catch(Exception ex){
          JOptionPane.showMessageDialog(this, "Error al crear el cupón: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);  
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

