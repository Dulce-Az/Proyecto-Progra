    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String usuario=jTextField1.getText();
        String password=jTextField2.getText();
        
        boolean existe =false;
        
        for(Usuario u: Proyecto_A.usuarios){
            if(u.usuario.equals(usuario) && u.password.equals(password)){
                existe=true;
                if(u.rol.equals("A")){
                    VentanaAdministrador v=new VentanaAdministrador(u);
                    v.setVisible(true);
                }
                else{
                    VentanaVendedor v=new VentanaVendedor(u);
                    v.setVisible(true);
                }
            }
        }
        if(existe == false){
            JOptionPane.showMessageDialog(this, "Usuario o passwor invalido");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

